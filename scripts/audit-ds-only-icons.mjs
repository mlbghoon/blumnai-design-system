#!/usr/bin/env node

/**
 * Phase 0 audit: find DS registry keys that have NO corresponding @remixicon/react export.
 *
 * Strategy:
 * 1. Parse Remixicon's tags.json (canonical category â†’ kebab-case base names)
 * 2. Build the set of predicted DS registry keys for every Remixicon icon
 *    (kebab â†’ no-hyphen lowercase, with optional 'fill' suffix and '_' prefix for numeric leads)
 * 3. Parse DS ui-icon-registry.tsx â†’ extract registered keys
 * 4. Diff: any DS key not in the predicted set is DS-only
 *
 * Outputs:
 * - audit-report.md (human-readable summary by category)
 *
 * Manual review of audit-report.md â†’ decide per icon: (a) move to overlay, (b) drop, (c) submit upstream.
 *
 * Reads tags.json from a vendored path or fetches at run time.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');

const TAGS_PATH_CANDIDATES = [
  path.join(ROOT, 'scripts', 'data', 'remix-tags.json'),
  '/tmp/remix-tags.json',
];
const REGISTRY_PATH = path.join(ROOT, 'src/components/icons/Icon/legacy/ui-icon-registry.tsx');
const REPORT_PATH = path.join(ROOT, 'docs/icon-audit-report.md');

// ----- step 1: load Remixicon tags.json -----

async function loadTags() {
  for (const candidate of TAGS_PATH_CANDIDATES) {
    if (fs.existsSync(candidate)) {
      console.log(`Reading tags.json from ${candidate}`);
      return JSON.parse(fs.readFileSync(candidate, 'utf8'));
    }
  }
  throw new Error(
    `No vendored tags.json found. Audit aborted to avoid non-deterministic results.\n` +
    `Place a tags.json matching @remixicon/react@${process.env.npm_package_dependencies__remixicon_react ?? '4.x'} at one of:\n` +
    TAGS_PATH_CANDIDATES.map((p) => `  - ${p}`).join('\n') +
    `\n\nTo fetch a version-matched file manually:\n` +
    `  curl -L https://raw.githubusercontent.com/Remix-Design/RemixIcon/v4.x/tags.json -o scripts/data/remix-tags.json`,
  );
}

const tags = await loadTags();

// ----- step 2: build predicted DS registry key set -----

const kebabToRegistryKey = (s) => s.replace(/-/g, '').toLowerCase();

const remixiconCanonical = new Map(); // key â†’ { name, category, isFill }
for (const [categoryName, entries] of Object.entries(tags)) {
  if (categoryName.startsWith('_') || typeof entries !== 'object') continue;
  for (const baseName of Object.keys(entries)) {
    const key = kebabToRegistryKey(baseName);
    // Line variant
    remixiconCanonical.set(key, { name: baseName, category: categoryName, isFill: false });
    remixiconCanonical.set(key + 'fill', { name: baseName, category: categoryName, isFill: true });
    // Numeric-leading: DS prefixes with underscore
    if (/^\d/.test(baseName)) {
      remixiconCanonical.set('_' + key, { name: baseName, category: categoryName, isFill: false });
      remixiconCanonical.set('_' + key + 'fill', { name: baseName, category: categoryName, isFill: true });
    }
  }
}

console.log(`Remixicon canonical keys: ${remixiconCanonical.size}`);

// ----- step 3: parse DS registry -----

const registrySource = fs.readFileSync(REGISTRY_PATH, 'utf8');
// Each entry looks like: '_24hours': ['finance', '_24HoursIcon'],
const entryPattern = /^\s*'([^']+)':\s*\['([^']+)',\s*'([^']+)'\]/gm;
const dsEntries = [];
let match;
while ((match = entryPattern.exec(registrySource)) !== null) {
  dsEntries.push({ key: match[1], category: match[2], component: match[3] });
}

console.log(`DS registry entries: ${dsEntries.length}`);

// ----- step 4: diff -----

const dsOnly = dsEntries.filter(({ key }) => !remixiconCanonical.has(key));
console.log(`DS-only icons: ${dsOnly.length}`);

// ----- step 5: generate report -----

const byCategory = new Map();
for (const e of dsOnly) {
  if (!byCategory.has(e.category)) byCategory.set(e.category, []);
  byCategory.get(e.category).push(e);
}

const sortedCategories = [...byCategory.keys()].sort();

let md = '# DS-only Icons Audit Report\n\n';
md += `Generated by \`scripts/audit-ds-only-icons.mjs\` against Remixicon \`tags.json\` (master).\n\n`;
md += `## Summary\n\n`;
md += `- Remixicon canonical icons: ${remixiconCanonical.size / 2} families (each line + fill)\n`;
md += `- DS registry entries: **${dsEntries.length}**\n`;
md += `- DS-only icons (not in Remixicon): **${dsOnly.length}**\n\n`;

if (dsOnly.length === 0) {
  md += 'âś“ No DS-only icons. All registry entries are covered by Remixicon.\n\n';
  md += 'Phase 0 complete: no overlay needed. Phase 1 can proceed safely.\n';
} else {
  md += `## DS-only icons by category\n\n`;
  md += `For each icon below, decide: (a) move to \`custom-icons.ts\` overlay, (b) drop, (c) submit upstream.\n\n`;
  for (const cat of sortedCategories) {
    const items = byCategory.get(cat);
    md += `### ${cat} (${items.length})\n\n`;
    md += '| Registry key | Component name |\n';
    md += '|---|---|\n';
    for (const item of items) {
      md += `| \`${item.key}\` | \`${item.component}\` |\n`;
    }
    md += '\n';
  }
}

// Ensure docs dir exists
const docsDir = path.dirname(REPORT_PATH);
if (!fs.existsSync(docsDir)) fs.mkdirSync(docsDir, { recursive: true });

fs.writeFileSync(REPORT_PATH, md);
console.log(`\nWrote ${REPORT_PATH}`);
console.log(`\n=== Summary ===`);
console.log(`DS-only icons found: ${dsOnly.length}`);
if (dsOnly.length > 0) {
  console.log(`Categories with DS-only icons: ${sortedCategories.join(', ')}`);
}
