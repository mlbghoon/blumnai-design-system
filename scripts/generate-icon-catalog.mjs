#!/usr/bin/env node

/**
 * 아이콘 카탈로그 생성 스크립트
 *
 * 모든 통합 아이콘 파일을 스캔하여 두 개의 참조 파일을 생성합니다:
 * 1. ICONS.md — 개발자/AI 에이전트용 마크다운 참조
 * 2. src/components/icons/icon-catalog.ts — TypeScript 카탈로그
 */

import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const ICONS_DIR = path.join(ROOT, 'src', 'components', 'icons');
const CATALOG_TS_PATH = path.join(ICONS_DIR, 'icon-catalog.ts');
const ICONS_MD_PATH = path.join(ROOT, 'ICONS.md');

const EXPORT_REGEX = /export const (\w+Icon)\b/g;

function extractComponentNames(filePath) {
  if (!fs.existsSync(filePath)) return [];
  const content = fs.readFileSync(filePath, 'utf-8');
  const names = [];
  let match;
  while ((match = EXPORT_REGEX.exec(content)) !== null) {
    names.push(match[1]);
  }
  EXPORT_REGEX.lastIndex = 0;
  return names.sort();
}

function extractFromDirectory(dirPath, globPattern) {
  if (!fs.existsSync(dirPath)) return [];
  const files = fs.readdirSync(dirPath).filter(f => f.match(globPattern));
  const allNames = [];
  for (const file of files) {
    allNames.push(...extractComponentNames(path.join(dirPath, file)));
  }
  return allNames.sort();
}

// UI icon categories
const UI_CATEGORIES = [
  { subpath: 'arrows', file: 'arrows.tsx' },
  { subpath: 'buildings', file: 'buildings.tsx' },
  { subpath: 'business', file: 'business.tsx' },
  { subpath: 'communication', file: 'communication.tsx' },
  { subpath: 'design', file: 'design.tsx' },
  { subpath: 'development', file: 'development.tsx' },
  { subpath: 'device', file: 'device.tsx' },
  { subpath: 'document', file: 'document.tsx' },
  { subpath: 'editor', file: 'editor.tsx' },
  { subpath: 'finance', file: 'finance.tsx' },
  { subpath: 'food', file: 'food.tsx' },
  { subpath: 'health', file: 'health & medical.tsx' },
  { subpath: 'logos', file: 'logos.tsx' },
  { subpath: 'map', file: 'map.tsx' },
  { subpath: 'media', file: 'media.tsx' },
  { subpath: 'others', file: 'others.tsx' },
  { subpath: 'system', file: 'system.tsx' },
  { subpath: 'user', file: 'user & faces.tsx' },
  { subpath: 'weather', file: 'weather.tsx' },
];

// Special icon types — single file
const SPECIAL_SINGLE = [
  { subpath: 'flags', dir: 'FlagIcon', file: 'icons/all.tsx' },
  { subpath: 'file-icons', dir: 'FileIcon', file: 'icons/all.tsx' },
  { subpath: 'cursors', dir: 'CursorIcon', file: 'icons/all.tsx' },
];

// Special icon types — chunked across multiple files
const SPECIAL_CHUNKED = [
  { subpath: 'brands', dir: 'BrandIcon', iconsDir: 'icons', pattern: /^brands-.*\.tsx$/ },
  { subpath: 'isometric', dir: 'IsometricIcon', iconsDir: 'icons', pattern: /^iso-.*\.tsx$/ },
];

const PKG_NAME = '@mbisolution/blumnai-design-system';

const catalog = {};
let totalCount = 0;

// Scan UI icon categories
for (const { subpath, file } of UI_CATEGORIES) {
  const filePath = path.join(ICONS_DIR, 'Icon', 'icons', file);
  const names = extractComponentNames(filePath);
  catalog[subpath] = names;
  totalCount += names.length;
}

// Scan special icon types (single file)
for (const { subpath, dir, file } of SPECIAL_SINGLE) {
  const filePath = path.join(ICONS_DIR, dir, file);
  const names = extractComponentNames(filePath);
  catalog[subpath] = names;
  totalCount += names.length;
}

// Scan special icon types (chunked)
for (const { subpath, dir, iconsDir, pattern } of SPECIAL_CHUNKED) {
  const dirPath = path.join(ICONS_DIR, dir, iconsDir);
  const names = extractFromDirectory(dirPath, pattern);
  catalog[subpath] = names;
  totalCount += names.length;
}

// Generate icon-catalog.ts
const tsCatalogEntries = Object.entries(catalog)
  .map(([key, names]) => {
    const namesList = names.map(n => `  '${n}',`).join('\n');
    return `  '${key}': [\n${namesList}\n  ],`;
  })
  .join('\n');

const tsCatalog = `// Auto-generated by scripts/generate-icon-catalog.mjs — Do not edit manually.
export const iconCatalog = {
${tsCatalogEntries}
} as const;

export type IconCatalog = typeof iconCatalog;
export type IconCategory = keyof IconCatalog;
`;

fs.writeFileSync(CATALOG_TS_PATH, tsCatalog);
console.log(`Generated icon-catalog.ts with ${totalCount} icons across ${Object.keys(catalog).length} categories.`);

// Generate ICONS.md — only regenerate the catalog section, preserve manual edits
const CATALOG_START = '## Available Icons';
const CATALOG_END = '<!-- END_AUTO_GENERATED_CATALOG -->';

const categoryDocs = Object.entries(catalog)
  .map(([key, names]) => {
    const importPath = `${PKG_NAME}/icons/${key}`;
    return `### ${key} (${names.length} icons)\n\n\`import from '${importPath}'\`\n\n${names.join(', ')}\n`;
  })
  .join('\n');

const generatedCatalog = `${CATALOG_START}\n\n${categoryDocs}\n${CATALOG_END}`;

let iconsMd;
if (fs.existsSync(ICONS_MD_PATH)) {
  const existing = fs.readFileSync(ICONS_MD_PATH, 'utf-8');
  const startIndex = existing.indexOf(CATALOG_START);
  const endIndex = existing.indexOf(CATALOG_END);

  // 카운트 라인 업데이트
  const updateCounts = (text) => text
    .replace(/\*\*\d+ icons\*\*/, `**${totalCount} icons**`)
    .replace(/across \d+ categories/, `across ${Object.keys(catalog).length} categories`);

  if (startIndex !== -1 && endIndex !== -1) {
    const before = updateCounts(existing.slice(0, startIndex));
    const after = existing.slice(endIndex + CATALOG_END.length);
    iconsMd = `${before}${generatedCatalog}${after}`;
  } else if (startIndex !== -1) {
    const before = updateCounts(existing.slice(0, startIndex));
    iconsMd = `${before}${generatedCatalog}\n`;
  } else {
    iconsMd = `${updateCounts(existing)}\n\n${generatedCatalog}\n`;
  }
} else {
  iconsMd = `# Icon Reference

> Auto-generated by \`scripts/generate-icon-catalog.mjs\` — Do not edit the catalog section manually.
>
> **${totalCount} icons** across ${Object.keys(catalog).length} categories.

## How to Use

See README for usage instructions.

${generatedCatalog}
`;
}

fs.writeFileSync(ICONS_MD_PATH, iconsMd);
console.log(`Generated ICONS.md.`);
